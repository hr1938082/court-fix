  <section class="lg-padding" id="criminal_justice_system">
    <div class="aos-item" data-aos="fade-up" data-aos-duration="350" data-aos-delay="0" data-aos-easing="ease-out" data-aos-anchor-placement="top-bottom">
      <div class="aos-item__inner">
        <div class="card">
          <div class="container container-header">
            <div class="title pt-4"> <span>NYS Criminal Justice System</span>
              <div class="subtitle"><span>An Overview</span></div>
            </div>
          </div>
          <div class="container">
            <div class="content">
              <div class="description left margin container-header">
                <div class="text text-dark">
                  <p>The NYS DCJS primarily serves as a resource for law enforcement professionals and those working in the criminal justice sector and public safety sector whom provide services for victims of crimes, offenders, and former offenders. The agency also serves the general public, and provides information to researchers and employees. Some such programs include Registered Sex Offender information, Criminal History Information, Criminal Justice Statistics and Information, and Security Guard Training.</p>
                  <p>There are three types of crimes that are defined by New York State law: felonies, misdemeanors, and violations.</p>
                  <h4 class="mt-5">Felonies</h4>
                  <p>Typically known as the most serious type of crime, Felonies in New York State include crimes like murder, rape and arson. If found guilty, felony offenders may face at least one year in prison or jail, and more if appropriate for the severity of the crime. Because New York does not have a death penalty, the most severe punishment is life without parole. Felonies are divided into groups to indicate the seriousness of the crime.</p>
                  <p>A-I felonies are violent, and carry a minimum 15-to-25-year imprisonment, and a maximum of life imprisonment without parole, except for drug crimes.</p>
                  <p>A-II felonies are violent, and can also be punishable by life in prison, except for drug crimes, but the minimum sentencing is between three and eight years and four months. In the case of sexually predatory offenders, this can be raised to between 10 and 25 years.</p>
                  <p>B&C felonies can be both violent and non-violent, and carry a minimum sentence of no less than one year and no more than 33 percent of the maximum term imposed on the given crime. The court is responsible for determining the actual time of imprisonment imposed within these parameters. These felonies usually involve violent assault, armed robbery, larceny, drug trafficking, or distribution, fraud, rape or alleged homicide.</p>
                  <p>D&E felonies can be both violent and non-violent, and carry the same minimum sentencing rules as Class B&C felonies, with the possibility of an alternative sentencing. If the judge opts to use these alternative sentences, and once the nature of the crime and character of the offender has been revealed, they may choose to issue a sentence of under one year in prison. These felonies typically include abandonment of child, aggravated assault (upon a minor), arson through recklessness(4th degree arson), theft, fraud, burglary, and manslaughter.</p>
                  <h4 class="mt-5">Misdemeanor</h4>
                  <p>A misdemeanor is a less serious crime in New York, but more serious than a violation. The most that an offender can be punishable by, for even the most serious misdemeanors, is one year in prison. Common misdemeanors include graffiti tagging and prostitution. They are divided into three different categories.</p>
                  <p>Class A misdemeanors are the most serious type of misdemeanors, but are still lightly punishable relative to felonies. Class A misdemeanors are punishable by up to one year in prison.</p>
                  <p>Class B misdemeanors are punishable by up to 3 months in jail.</p>
                  <p>Unclassified misdemeanors represent a wide category for misdemeanors, and are designated to crimes that do not fit into any other general category. The crimes they punish are often unique or subject to new laws or issues that have not yet been addressed. This can include gambling offenses, littering, or certain DWIs. They are typically punishable by up to three years of probation, but for certain crimes can be punished by up to one year in jail.</p>
                  <h4 class="mt-5">Violations</h4>
                  <p>Violations are the least serious type of crime in New York State and include crimes such as trespassing and disorderly conduct. The most someone can be punished for a violation is 15 days in jail.</p>
