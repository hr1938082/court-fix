<section class="lg-padding" id="overview">
  <div class="aos-item" data-aos="fade-up" data-aos-duration="350" data-aos-delay="0" data-aos-easing="ease-out" data-aos-anchor-placement="top-bottom">
    <div class="aos-item__inner">
      <div class="container">
        <div class="card">
          <div class="container pt-4 pb-4"><span>Accused</span>
            <p class="text-dark mt-2 italic">The person against whom an accusation is made; one who is charged with a crime or traffic infraction</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Adult Drug Treatment Court</span>
            <p class="text-dark mt-2 italic">Adult Drug treatment courts are usually managed by a nonadversarial and multidisciplinary team including judges, prosecutors, defense attorneys, community corrections, social workers and treatment service professionals. Support from stakeholders representing law enforcement, the family and the community is encouraged through participation in hearings, programming and events like graduation.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Appeal</span>
            <p class="text-dark mt-2 italic">Taking a case which has been decided in a court of inferior jurisdiction to one of superior jurisdiction, for the purpose of obtaining a review</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Arraign</span>
            <p class="text-dark mt-2 italic">Arraignment of an accused consists of calling upon him by name, reading to him the charges in the arrest documents, demanding of him whether he pleads guilty or not guilty or, in misdemeanors, no lo contendere, and entering his plea. This hearing may be combined with right to counsel hearing</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Bail</span>
            <p class="text-dark mt-2 italic">The release of a person from legal custody by a written agreement that he shall appear at the time and place designated and submit himself to the jurisdiction of the court and observe the requirements set forth in the recognizance.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Bond</span>
            <p class="text-dark mt-2 italic">A certificate or evidence of a debt with a sum fixed as a penalty, which contains a written agreement binding the parties to pay the debt, conditioned, however, that the payment of the penalty may be avoided by the performance of certain acts by one or more of the parties</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Buprenorphine</span>
            <p class="text-dark mt-2 italic">A medication used sublingually and in other forms (e.g., in combination with naloxone as Suboxone), buprenorphine is a partial opioid agonist that helps to curb cravings and withdrawal symptoms without producing the euphoria or sedation associated with the abused opioid drugs.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Case Manager</span>
            <p class="text-dark mt-2 italic">Case Managers performs initial screenings on referred respondents, link them with treatment, and provide the ongoing reports on participant compliance with their chemical dependency and recovery programs. The Case Managers monitor all areas of treatment to ensure that the program is delivering the proper level of treatment and services needed during all phases of the program. The Case Managers act as liaisons between Drug Court and treatment providers.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Civil action </span>
            <p class="text-dark mt-2 italic">A case brought for determination enforcement or protection of a right, or redress; or prevention of a wrong; every action other than a criminal action.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Complaint </span>
            <p class="text-dark mt-2 italic">Criminal - A charge brought before a judicial officer having jurisdiction, that a person named has committed a specified offense.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Coordinator – Adult Drug Treatment Court</span>
            <p class="text-dark mt-2 italic">An adult drug treatment court coordinator oversees the activity of the team, conducts quality assurance of each team member, maintains client data, remains informed regarding budgetary concerns of the drug court and coordinates services from each discipline, and the local community, in a manner that is most therapeutic to the drug court participant.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Court order </span>
            <p class="text-dark mt-2 italic">A command or mandatory direction of a judge which is made during a case. Also includes a command of the judge which establishes courtroom or administrative procedures.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Crime</span>
            <p class="text-dark mt-2 italic">A positive or negative act in violation of penal law; an offense against the state classified either as a felony or misdemeanor</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Custody</span>
            <p class="text-dark mt-2 italic">The detainment of a person by virtue of lawful process or authority; actual imprisonment</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Defendant</span>
            <p class="text-dark mt-2 italic">The party against whom relief or recovery is sought in a court action or suit. Sometimes used to designate the accused in criminal or traffic cases</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Defense Counsel – Adult Drug Treatment Court</span>
            <p class="text-dark mt-2 italic">An adult drug treatment court defense counsel informs the drug court participant about the rigors of drug court, preserves all legal rights of the client, advocates for fair and equal treatment of client, participates in team meetings and attends non-adversarial court proceedings.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Deposition</span>
            <p class="text-dark mt-2 italic">The testimony of a witness taken upon oral examination, after notice to the adverse party, not in open court, but in pursuance of a notice to take testimony issued by the party wanting the deposition. The adverse party has the right to attend and cross-examine. Testimony is reduced to writing and duly authenticated, and intended to be used in connection with the trial of an action in court. These are used in circuit court.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Detention </span>
            <p class="text-dark mt-2 italic">The holding of a person in custody or confinement</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Discovery</span>
            <p class="text-dark mt-2 italic">Procedures by which one party to a lawsuit may obtain information relevant to the case which is held or known by the other party</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Dismissal </span>
            <p class="text-dark mt-2 italic">An order disposing of an action, suit, etc., without trial</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Dismissed but not sealed</span>
            <p class="text-dark mt-2 italic">An order disposing of an action without trial but the file can be physically opened for review.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Disposition</span>
            <p class="text-dark mt-2 italic">Determination of the final arrangement or settlement of a case following judgment.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Docket </span>
            <p class="text-dark mt-2 italic">A record of all cases and actions scheduled to be heard in court, whether or not the matter is actually heard in a court on a particular day.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Drug Testing</span>
            <p class="text-dark mt-2 italic">Drug testing is the evaluation of urine, blood or another type of biological sample to determine if the subject has been using the drug or drugs in question.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Drug Treatment Court Practitioner</span>
            <p class="text-dark mt-2 italic">Comprised of judges, clinicians, defense attorneys, prosecutors, program coordinators, probation officers, police officers, vocational rehabilitation counselors, housing and education officials, and other treatment and justice representatives who provide important services in facilitating a participant’s recovery. Each practitioner represents an essential part of the drug treatment court program and provides important input into the intertwined process of treatment, supervision, and accountability.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Evidence</span>
            <p class="text-dark mt-2 italic">All the means by which a matter of fact, the truth of which is submitted for investigation, is established or disproved.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Felony</span>
            <p class="text-dark mt-2 italic">A crime punishable by death or confinement in the penitentiary. See § 18.2-10 for classification of felonies and the punishment for each classification.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Finding</span>
            <p class="text-dark mt-2 italic">The result of the deliberations of a court.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Garnishment</span>
            <p class="text-dark mt-2 italic">A statutory post-judgment proceeding in which a third party who holds property, money or credits belonging to the judgment debtor is required to surrender such property, money or credits (to the extent of the judgment) to the court or sheriff for application against the judgment awarded against the judgment debtor.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Graduation and Termination</span>
            <p class="text-dark mt-2 italic">Drug Treatment Court graduates are given the opportunity to speak about their experience in Drug Treatment Court and how it has impacted their recovery.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Grand Jury</span>
            <p class="text-dark mt-2 italic">A special type of jury assembled to investigate whether criminal charges should be brought. Grand jury proceedings are supervised by circuit courts.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Guilty</span>
            <p class="text-dark mt-2 italic">Responsible for committing a criminal offense or a traffic infraction. The word used by an accused in pleading to the charges when he confesses to committing the crime of which he is charged. It is also used by the judge if he finds that the accused committed a criminal offense or a traffic infraction.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Incentives and Sanctions</span>
            <p class="text-dark mt-2 italic">The purpose of sanctions and incentives is to keep participants engaged in treatment. Incentives (positive and negative reinforcement) | Sanctions (punishment or response cost). Punishment is NOT the goal in the Imposition of Sanctions.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Indictment</span>
            <p class="text-dark mt-2 italic">A formal accusation by a grand jury that charges a person with a crime. Indictments are used to bring more serious charges and are used in circuit court only.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Judge –Adult Drug Treatment Court</span>
            <p class="text-dark mt-2 italic">A drug treatment court judge presides over non-adversarial court appearances for drug treatment court appearances and leads the drug treatment court team in creating a participant focused recovery program.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Legal Aid</span>
            <p class="text-dark mt-2 italic">Legal services are available in some areas to persons unable to afford an attorney. Legal aid offices handle only civil matters.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Medication-Assisted Treatment (MAT)</span>
            <p class="text-dark mt-2 italic">The use of medications specifically for the treatment of substance use disorders.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Methadone</span>
            <p class="text-dark mt-2 italic">This long-acting oral opioid medication helps to reduce the withdrawal and cravings associated with more potent abused opioid drugs like heroin. You must visit a clinic to be administered methadone.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Misdemeanor </span>
            <p class="text-dark mt-2 italic">Offenses punishable by fine not exceeding $2,500 or being jailed for a term not exceeding 12 months or a combination of fine and jail within these limits.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Mistrial </span>
            <p class="text-dark mt-2 italic">A trial that is cut short and does not result in a verdict due to a procedural error or other problem. The trial must then start over from the beginning.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Motion</span>
            <p class="text-dark mt-2 italic">A request made to the judge by a litigant or other person connected with the case for a ruling or order.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Naltrexone</span>
            <p class="text-dark mt-2 italic">Rather than acting in a way similar to other opioids in the body, naltrexone prevents opioid drugs from binding to their respective opioid receptor sites in the brain and, in doing so, blocks their effects. This substance is available as an oral medication or an injection that lasts for a month.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Notice</span>
            <p class="text-dark mt-2 italic">Formal notification of a legal proceeding or determination.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Parole</span>
            <p class="text-dark mt-2 italic">In criminal law, a conditional release. If prisoner makes good, he will receive an absolute discharge from balance of sentence, but, if he does not, he will be returned to serve unexpired time.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Petition</span>
            <p class="text-dark mt-2 italic">A formal request to a court to take a certain action on a matter.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Pharmacotherapies</span>
            <p class="text-dark mt-2 italic">Medications utilized to specifically manage various substance dependencies, ease symptoms of withdrawal, prevent relapse, and treat co-occurring mental health conditions that complicate substance use.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Phase Advancement</span>
            <p class="text-dark mt-2 italic">Phases are based on clinical needs and the prognostic risk factors found in the local court’s population. Phases give the client structure and visible steps to measure progress. Phases are completed by using objective criteria to move the client along the road to a mature recovery and a pro-social lifestyle. The criteria may be either specific objective achievements or there may be a point system to measure readiness for phase advancement. This provides the client a way to measure individual growth and success in the program.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Plaintiff</span>
            <p class="text-dark mt-2 italic">A person who brings an action; the party who complains or sues in a personal action and is so named on the record.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Plea</span>
            <p class="text-dark mt-2 italic">Statement made by the defendant either as to his guilt or innocence to the charge made against him.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Probation</span>
            <p class="text-dark mt-2 italic">In modern criminal administration, allowing a person convicted of some offense to remain free under a suspension of a jail sentence during good behavior and generally under the supervision or guardianship of probation officer together with other restrictions as the court may impose.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Prosecutor – Adult Drug Treatment Court</span>
            <p class="text-dark mt-2 italic">An adult drug treatment court prosecutor is usually a “gate keeper” and selects persons of substance use disorder who participate in the program; he/she obtains prior criminal histories of offenders, participates in team meetings and attends non-adversarial court proceedings.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Remand</span>
            <p class="text-dark mt-2 italic">Sending a case back to the same court out of which it came for purpose of having some action taken on it there.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Sealed </span>
            <p class="text-dark mt-2 italic">A file that is physically closed from review.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Search warrant</span>
            <p class="text-dark mt-2 italic">An order in writing, issued by a judicial officer, in the name of the state, directed to a sheriff, or other officer commanding him to conduct a search to aid an official investigation.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Seizure</span>
            <p class="text-dark mt-2 italic">To take into possession forcibly.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Sentence</span>
            <p class="text-dark mt-2 italic">The judgment formally pronounced by the judge upon the defendant after his conviction in a criminal prosecution, setting the punishment for the offense.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Subpoena</span>
            <p class="text-dark mt-2 italic">A process to cause a witness to appear and give testimony, commanding him to appear before a court therein named at a time therein mentioned to testify for the party named under a penalty therein mentioned.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Substance Use Disorder</span>
            <p class="text-dark mt-2 italic">Substance use disorders occur when the recurrent use of alcohol and/or drugs causes clinically significant impairment, including health problems, disability, and failure to meet major responsibilities at work, school, or home.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Termination</span>
            <p class="text-dark mt-2 italic">The purpose is to give the individual a “taste” of detention without incurring the costs of transportation or having the individual processed into the jail. The ultimate sanction in Drug Court ensues from an unsuccessful termination.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Trauma-informed Care</span>
            <p class="text-dark mt-2 italic">Trauma-Informed Care understands and considers the pervasive nature of trauma and promotes environments of healing and recovery rather than practices and services that may inadvertently re-traumatize.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Treatment Provider – Adult Drug Treatment Court</span>
            <p class="text-dark mt-2 italic">An adult drug treatment court treatment provider provides rehabilitative therapy sessions, drug screening, case management and monitoring for drug court participants in keeping with the holistic recovery of the drug court participant. Additionally, within the bounds of ethics and legalities, a drug court treatment provider shares information regarding the progress of a participant in appropriate settings to all drug court team members.</p>
          </div>
        </div>
        <div class="card">
          <div class="container pt-4 pb-4"><span>Warrant of Arrest</span>
            <p class="text-dark mt-2 italic">A written order issued and signed by a judicial officer directed to a law enforcement officer or some other person specially named and commanding him to arrest the body of a person named in it who is accused of an offense.</p>
          </div>
        </div>
